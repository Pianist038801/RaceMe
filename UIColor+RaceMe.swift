import UIKit

extension UIColor {
    
    class func raceMeRedColor () -> UIColor {
        return UIColor(red:0.95, green:0.31, blue:0.32, alpha:1.0)
    }
    
    class func raceMeOrangeColor () -> UIColor {
        return UIColor(red:0.97, green:0.52, blue:0.16, alpha:1.0)
    }
    
    class func raceMeNeonGreenColor () -> UIColor {
        return UIColor(red:0.90, green:0.85, blue:0.29, alpha:1.0)
    }
    
    class func raceMeTextBlueColor () -> UIColor {
        return UIColor(red:0.35, green:0.75, blue:0.80, alpha:1.0)
    }
    
    class func raceMeMutedGreyColor () -> UIColor {
        return UIColor(red:0.73, green:0.71, blue:0.64, alpha:1.0)
    }
    
    class func raceMeLightNavyColor () -> UIColor {
       return UIColor(red:0.07, green:0.27, blue:0.40, alpha:1.0)
    }
    
}